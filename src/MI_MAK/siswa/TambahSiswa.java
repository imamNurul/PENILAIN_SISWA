    protected String validateNIP(String nip) {
        NIP = null;
        try {
            String sql = "SELECT nomor_induk FROM tbl_siswa where nomor_induk = '"+nip+"'";
            Statement st = koneksi.createStatement();
            ResultSet rs = st.executeQuery(sql);
          
            while (rs.next()) {
                NIP = rs.getString("nomor_induk");
                
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "validate NIP gagal" + e);
        }
        
        return NIP;
    }
    

