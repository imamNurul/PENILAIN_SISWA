    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if(txtNoInduk.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nomor Induk Kosong");
        }else if(txtNamaSiswa.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nama Siswa Kosong");
        }else if(txtTmptLahir.getText().trim().isEmpty()){
            messageComponent1.showWarning("Tempat Lahir Kosong");
        }else if(dateLahir.getDate() ==null){
            messageComponent1.showWarning("Tanggal Lahir Kosong");
        }else if(comboAgama.getSelectedItem().equals("Pilih")){
            messageComponent1.showWarning("Agama belum dipilih");
        }else if(txtAlamat.getText().trim().isEmpty()){
            messageComponent1.showWarning("Alamat Siswa kosong");
        }else if(txtThnMasuk.getValue().equals(0)){
            messageComponent1.showWarning("Tahun masuk kosong");
        }else if(txtNamaAyah.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nama Ayah kosong");
        }else if(txtNamaIbu.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nama Ibu kosong");
        }else if(comboKelas.getSelectedItem().equals("Pilih")){
            messageComponent1.showWarning("Kelas Belum dipilih");
        }else{
            
            validateNIP(txtNoInduk.getText());
            if(NIP != null){
                messageComponent1.showWarning("Nomor Induk sudah ada");
            }else{
                Siswa siswa = new Siswa();
            siswa.setAgama(comboAgama.getSelectedItem().toString());
            siswa.setAlamat_siswa(txtAlamat.getText());
            siswa.setCreatedby("Admin");
            siswa.setCreateddate(new java.sql.Timestamp(new java.util.Date().getTime()));
            siswa.setFlag(1);
            
            if(rbLaki.isSelected()== true){
                siswa.setJenis_kelamin("L");
            }else{
                siswa.setJenis_kelamin("P");
            }
            
            siswa.setKj_ayah(txtKj_Ayah.getText());
            siswa.setKj_ibu(txtKj_Ibu.getText());
            siswa.setNama_ayah(txtNamaAyah.getText());
            siswa.setNama_ibu(txtNamaIbu.getText());
            siswa.setNama_siswa(txtNamaSiswa.getText());
            siswa.setNomor_induk(txtNoInduk.getText());
            siswa.setPhoto(txtAlamatPhoto.getText());
            siswa.setTahun_masuk(new Integer(txtThnMasuk.getValue().toString()));
            siswa.setTanggal_lahir(dateLahir.getDate());
            siswa.setTelp_wali(txtTlpWali.getText());
            siswa.setTempat_lahir(txtTmptLahir.getText());
            siswa.setKode_kelas(lblKodeKelas.getText());
            siswa.setNama_kelas(lblNamaKelas.getText());
            
            SiswaService siswaService = new SiswaService();
            
            siswaService.insert(siswa);
            dispose();
            }
            
            
            
            
            
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

