    public Penilaian ubahNilai(Penilaian param){
        
        
        txtId_nilai.setText(param.getId_nilai());
        txtMapel.setText(param.getKd_mapel()+"-"+param.getNm_mapel());
        txtKelas.setText(param.getKd_kelas()+"-"+param.getNm_kelas());
        labelKdKls.setText(param.getKd_kelas());
        labelNmKls.setText(param.getNm_kelas());
        labelKdMapel.setText(param.getKd_mapel());
        labelNmMapel.setText(param.getNm_mapel());
        txtThnAjaran.setText(param.getThnAjaran());
        txtSemester.setText(param.getSemester());
        labelID.setText(String.valueOf(param.getId()));
        
        nilai = param;
        
        setLocationRelativeTo(this);
        setVisible(true);
        
        return nilai;
        
    }
    
    
