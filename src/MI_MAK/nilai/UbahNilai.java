    public void LoadNilaiDetailUbah(String id){
        
        new SwingWorker<List<PenilaianDetail>, Object>(){

            @Override
            protected List<PenilaianDetail> doInBackground() throws Exception {
                
                Thread.sleep(1000);
                List<PenilaianDetail> list = service.getUbahNilaiDetail(id);

                return list;
            }

            @Override
            protected void done() {
                try {
                    tableModel.clear();
                    for(PenilaianDetail jsb : get()){
                    tableModel.add(jsb);
                }
                } catch (InterruptedException | ExecutionException ex) {
                    Logger.getLogger(UbahNilai.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
  
        }.execute();
        
    }

