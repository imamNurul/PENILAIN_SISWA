    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if(txtMapel.getText().trim().isEmpty()){
            messageComponent1.showWarning("Mapel Kosong");
        }else if(txtKelas.getText().trim().isEmpty()){
            messageComponent1.showWarning("Kelas belum dipilih");
        }else{
            
            try {
                Penilaian penilaian = new Penilaian();
                
                penilaian.setUpdateBy("Admin");
                penilaian.setUpdateDate(new java.sql.Timestamp(new java.util.Date().getTime()));
                penilaian.setId_nilai(txtId_nilai.getText());
                penilaian.setKd_kelas(labelKdKls.getText());
                penilaian.setKd_mapel(labelKdMapel.getText());
                penilaian.setNm_kelas(labelNmKls.getText());
                penilaian.setNm_mapel(labelNmMapel.getText());
                penilaian.setSemester(txtSemester.getText());
                penilaian.setThnAjaran(txtThnAjaran.getText());
                penilaian.setId(Integer.parseInt(labelID.getText()));
                
                PenilaianService service1 = new PenilaianService();
                service1.update(penilaian);
                
                String sql = "DELETE FROM tbl_nilai_detail WHERE id_nilai = '"+txtId_nilai.getText()+"'";
                PreparedStatement pss = konek.prepareStatement(sql);
                pss.executeUpdate();
                
                String sql2 = "INSERT INTO tbl_nilai_detail(id_nilai, kd_siswa, nama_siswa, "
                        + "UH, UTS, UAS) "
                        +"VALUES(?,?,?,?,?,?)";
                PreparedStatement ps = konek.prepareStatement(sql2);
                
                for(int i = 0; i < tableModel.getRowCount(); i++){
                    PenilaianDetail detail = tableModel.get(i);
                    ps.setString(1, txtId_nilai.getText());
                    ps.setString(2, detail.getKd_siswa());
                    ps.setString(3, detail.getNm_siswa());
                    ps.setDouble(4, detail.getUH());
                    ps.setDouble(5, detail.getUTS());
                    ps.setDouble(6, detail.getUAS());
                    
                    ps.executeUpdate();
                    
                }
                
                
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(UbahNilai.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

